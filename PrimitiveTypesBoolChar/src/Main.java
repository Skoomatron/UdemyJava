public class Main {
    public static void main(String[] args) {

        char charOne = 'f'; // only stores one character
        char uniCode = '\u00D4'; // \u must precede all unicode characters

        System.out.println("Char one = " + charOne);
        System.out.println("Char two = " + uniCode);


        boolean boolOne = true;
        boolean boolTwo = false;

        System.out.println("Boolean one = " + boolOne);
        System.out.println("Boolean two = " + boolTwo);
    }
}