public class Main {
    public static void main(String[] args) {
        int testValue = 1;

        switch(testValue) {
            case 1:
                System.out.println("Value is 1");
                break;
            case 2:
                System.out.println("Value is 2");
                break;
            default:
                System.out.println("No value found");
                break;
        }
    }
}